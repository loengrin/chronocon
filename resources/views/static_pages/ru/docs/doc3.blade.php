    (Например для государств возможен набор ‘Возникновение‘, ‘Исчезновение‘, ‘Форма правления‘, ‘Правитель‘, ‘Династия‘, ‘Религия‘, ‘Текущее положение‘).
    Чтобы это ускорить сделаны шаблоны таблиц. Можно один раз настроить шаблон и На странице редактирования таблиц применить его. Шаблоны редактируются по ссылке в панели редактирования.
    При наведении мышкой на объект на карте будет показывать объединенная таблица.
