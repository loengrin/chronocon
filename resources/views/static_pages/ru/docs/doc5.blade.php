    События делают нашу карту не просто картой интерактивной, а именно "динамической".<br />
    Переход между событиями меняет содержимое левого блокаи меняет положение карты.<br />
    Навигация по событиям происходит с помошью кнопок "Вперёд" и "Назад".
    Причём навигация возможна в двух режимах - в хронологическом порядке и по сюжетам. О группировке событий в сюжеты будет рассказано ниже.
    Событию может соответствовать маркер на карте, указывающий на место события.
