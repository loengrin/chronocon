    <h2>Цепочки событий</h2>
    После создания первых интерактивных карт обнаружилась проблема. Если не все события связаны друг с другом, то листать их в хронологическом порядке неудобно. Приходится постоянно переключать внимание между разными смысловыми цепочками. Дле решения проблемы были введены "Цепочки событий"(или "Сюжеты").<br />
    Можно создать несколько цепочек и распределить события между ними. Каждое событие может принадлежать к нескольким цепочкам. Например в карте XIII века Куликовская битва принадлежит к двум цепочкам - "Монгольская империя" и "Древняя Русь".<br />
    Если в карте есть цепочки событий(хотя бы одна), то кроме содержания в хронологическом порядке ("по датам"), будет доступно содержание "по сюжетам". 


    <h2>Маркер и положение карты</h2>
    При переходе между событиями может меняться положение карты. Положение карты должно быть таким чтобы на экран попадало как можно больше объектов, относящихся к событию. При создании нового события положение карты устанавливается равным текущему положению.
    Если опция "Показывать маркер" включена, положение карты будет такое, чтобы маркер находился в ее центре. Маркер можно перемещать.
    В меню, которое открывается при клике правой кнопкаой на маркер, можно выбрать "Установить масштаб карты". Тогда при переходе на это событие ката будет переходить в сохраненный масштаб.
    
    <img width="100%" src="/img/doc_ru/doc5screen5.png"/><br />
    Если опция "Показывать маркер" выключена, вместо маркера на экране будет красный круг. C помощь. этого круга можно задать положение карты так же, как при помощи карты.
    Только в режиме просмотра круг виден не будет.
