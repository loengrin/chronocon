Chronocon is a free editor of interactive historical maps. Everyone can create custom interactive map of any period of time.
<br> <br>
The main problem which has to decide the project - a demonstration of the context of events. Books and articles are typically described in any particular location at a particular time. To find out what happened next, you need to look elsewhere. There will also be visible to the whole picture of the world with the necessary reference information . 
<br> <br>
One of the possible project goal is to create a global interactive historical map that covers the whole human history. 
It can be done in two stages. The first stage - creation of maps covering a relatively small period of time. When accumulated enough individual maps, it is assumed the second stage - the union of maps into one large. 
<br> <br>
Maybe on this global map it will be possible to create different versions of the same events, representing diffrerent political positions.
It is possible to create not only historical maps, but maps of fantasy world. You can use custom image as a map background. 
Chronocon is open and noncommersial project. Like wikipedia. 
Project uses google maps api.
